public class Variables {
    public static void main(String args[]) {
        int num1 = 10;
        int num2 = 5;
        int result1, result2 = 0;
        double result3 = 0.0;

        result1 = num1 + num2;
        result2 = num1 * num2;
        result3 = num1 / num2;

        System.out.println("Addition: " + result1);
        System.out.println("Multiplication: " + result2);
        System.out.println("Division: " + result3);
    }
}