public class UpDown {
    public static void main(String args[]) {

        int a = 1;
        while (a <= 10) {
            System.out.print(a + " ");
            a++;
        }

        System.out.println("");

        for (int i = 10; i > 0; i--) {
            System.out.print(i + " ");
        }
    }
}